// Categories of Operators

// Arithmetic Operators
// Arithmetic operators perform mathematical calculations on numeric values or variables.

/*
+ (Addition)

- (Subtraction)

* (Multiplication)

/ (Division)

% (Modulus)

** (Exponentiation, ES2016+)

++ (Increment)

-- (Decrement) 
*/

// Addition (+)
let sum1 = 5 + 3; // 8
let sum2 = 10 + 20; // 30

// Subtraction (-)
let diff1 = 10 - 4; // 6
let diff2 = 7 - 15; // -8

// Multiplication (*)
let prod1 = 6 * 3; // 18
let prod2 = 2 * 8; // 16

// Division (/)
let div1 = 12 / 4; // 3
let div2 = 15 / 2; // 7.5

// Modulus (%)
let mod1 = 10 % 3; // 1
let mod2 = 14 % 5; // 4

// Exponentiation (**)
let exp1 = 2 ** 3; // 8
let exp2 = 5 ** 2; // 25

// Increment (++)
let a = 5;
a++; // 6
let b = 10;
++b; // 11

// Assignment Operators
// Assignment operators assign values to variables, often by combining an operation with the simple assignment (=)
/*
= (Simple assignment)

+=, -=, *=, /=, %= (Arithmetic assignments)

**= (Exponentiation assignment)

<<=, >>=, >>>= (Shift assignments)

&=, ^=, |= (Bitwise assignments)

&&=, ||=, ??= (Logical assignments, ES2020) 
*/

// Simple assignment (=)
let x = 10;
let y = "hello";

// Addition assignment (+=)
let num1 = 5;
num1 += 3; // 8
let str1 = "Hi, ";
str1 += "there!"; // "Hi, there!"

// Subtraction assignment (-=)
let num2 = 10;
num2 -= 4; // 6
let num3 = 20;
num3 -= 15; // 5

// Multiplication assignment (*=)
let num4 = 6;
num4 *= 3; // 18
let num5 = 2;
num5 *= 8; // 16

// Division assignment (/=)
let num6 = 12;
num6 /= 4; // 3
let num7 = 15;
num7 /= 2; // 7.5

// Modulus assignment (%=)
let num8 = 10;
num8 %= 3; // 1
let num9 = 14;
num9 %= 5; // 4

// Exponentiation assignment (**=)
let num10 = 2;
num10 **= 3; // 8
let num11 = 5;
num11 **= 2; // 25
